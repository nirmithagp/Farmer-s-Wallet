let currentLang = 'en';

// Initialize
document.addEventListener('DOMContentLoaded', function () {
    initNavbar();
    initLanguageSelector();
    initAnimations();
});

// Navbar scroll effect
function initNavbar() {
    const navbar = document.getElementById('navbar');

    window.addEventListener('scroll', function () {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // Mobile menu toggle
    const mobileMenuToggle = document.getElementById('mobileMenuToggle');
    const navMenu = document.getElementById('navMenu');

    if (mobileMenuToggle) {
        mobileMenuToggle.addEventListener('click', function () {
            navMenu.classList.toggle('active');
        });
    }
}

// Language selector
function initLanguageSelector() {
    const langBtn = document.getElementById('langBtn');
    const currentLangSpan = document.getElementById('currentLang');

    if (langBtn) {
        langBtn.addEventListener('click', function () {
            // Cycle through languages
            const languages = ['en', 'hi', 'kn', 'ta', 'te', 'ml'];
            const currentIndex = languages.indexOf(currentLang);
            const nextIndex = (currentIndex + 1) % languages.length;
            currentLang = languages[nextIndex];

            updateLanguage();
        });
    }
}

// Update language
function updateLanguage() {
    const langNames = {
        'en': 'English',
        'hi': 'हिंदी',
        'kn': 'ಕನ್ನಡ',
        'ta': 'தமிழ்',
        'te': 'తెలుగు',
        'ml': 'മലയാളം'
    };

    document.getElementById('currentLang').textContent = langNames[currentLang];

    // Update all translatable elements
    // Update all translatable elements
    document.querySelectorAll('[data-' + currentLang + ']').forEach(element => {
        // Prioritize inline attributes
        const inlineTranslation = element.getAttribute('data-' + currentLang);
        let textToSet = inlineTranslation;

        if (!textToSet) {
            // Fallback to dictionary
            const translationKey = element.getAttribute('data-en');
            if (translations[currentLang] && translations[currentLang][translationKey]) {
                textToSet = translations[currentLang][translationKey];
            }
        }

        if (textToSet) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = textToSet;
            } else {
                element.textContent = textToSet;
            }
        }
    });
}

// Scroll animations
function initAnimations() {
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -100px 0px'
    };

    const observer = new IntersectionObserver(function (entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    document.querySelectorAll('.card').forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(20px)';
        card.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(card);
    });
}

